import java.io.Serializable;

/**
 * Created by fede on 3/7/15.
 */
public class Person implements Serializable{

    private int id;
    private String name;

    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }

    @Override
    public String toString() {
        return "Person{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
